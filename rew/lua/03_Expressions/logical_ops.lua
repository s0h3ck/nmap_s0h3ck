-- and 
-- first argument as false
-- second argument as true
--
-- or
-- first argument as true
-- second argument as false
--

print(4 and 5)          --> 5
print(5 and 4)          --> 4
print(nil and 13)       --> nil
print(false and 13)     --> false
print(4 or 5)           --> 4
print(false or 5)       --> 5
print(5 or false)       --> 5
print(type(true))       --> boolean
